package kr.puri.entity;

public class Plant {
	int plant_seq;
	String plant_group;
	String plant_name;
	String plant_level;
	String plant_flower;
	String plant_size;
	String plant_water;
	String plant_sun;
	String plant_temp;
	String plant_feature;
	String plant_mean;
	String plant_img;
	
	public Plant() {}
	
	public Plant(int plant_seq, String plant_group, String plant_name, String plant_level, String plant_flower,
			String plant_size, String plant_water, String plant_sun, String plant_temp, String plant_feature,
			String plant_mean, String plant_img) {
		this.plant_seq = plant_seq;
		this.plant_group = plant_group;
		this.plant_name = plant_name;
		this.plant_level = plant_level;
		this.plant_flower = plant_flower;
		this.plant_size = plant_size;
		this.plant_water = plant_water;
		this.plant_sun = plant_sun;
		this.plant_temp = plant_temp;
		this.plant_feature = plant_feature;
		this.plant_mean = plant_mean;
		this.plant_img = plant_img;
	}

	public int getPlant_seq() {
		return plant_seq;
	}

	public void setPlant_seq(int plant_seq) {
		this.plant_seq = plant_seq;
	}

	public String getPlant_group() {
		return plant_group;
	}

	public void setPlant_group(String plant_group) {
		this.plant_group = plant_group;
	}

	public String getPlant_name() {
		return plant_name;
	}

	public void setPlant_name(String plant_name) {
		this.plant_name = plant_name;
	}

	public String getPlant_level() {
		return plant_level;
	}

	public void setPlant_level(String plant_level) {
		this.plant_level = plant_level;
	}

	public String getPlant_flower() {
		return plant_flower;
	}

	public void setPlant_flower(String plant_flower) {
		this.plant_flower = plant_flower;
	}

	public String getPlant_size() {
		return plant_size;
	}

	public void setPlant_size(String plant_size) {
		this.plant_size = plant_size;
	}

	public String getPlant_water() {
		return plant_water;
	}

	public void setPlant_water(String plant_water) {
		this.plant_water = plant_water;
	}

	public String getPlant_sun() {
		return plant_sun;
	}

	public void setPlant_sun(String plant_sun) {
		this.plant_sun = plant_sun;
	}

	public String getPlant_temp() {
		return plant_temp;
	}

	public void setPlant_temp(String plant_temp) {
		this.plant_temp = plant_temp;
	}

	public String getPlant_feature() {
		return plant_feature;
	}

	public void setPlant_feature(String plant_feature) {
		this.plant_feature = plant_feature;
	}

	public String getPlant_mean() {
		return plant_mean;
	}

	public void setPlant_mean(String plant_mean) {
		this.plant_mean = plant_mean;
	}

	public String getPlant_img() {
		return plant_img;
	}

	public void setPlant_img(String plant_img) {
		this.plant_img = plant_img;
	}
	
	

}
